package Models;

public class MachineType {
    
    int typeID;
    String typeName;
    
    public MachineType(int id, String name) {
        this.typeID = id;
        this.typeName = name;
    }
    

    public int getTypeID() {
        return typeID;
    }

    public void setTypeID(int typeID) {
        this.typeID = typeID;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }
    
    
}
